public class string {
    public static void main(String[] args) {
        String mystr = " java string";
        System.out.println("normal string without any function is :--"+mystr);
        //Java String toUpperCase() and toLowerCase() method
        System.out.println(mystr.toUpperCase());
        System.out.println(mystr.toLowerCase());
        //Java String trim() method
        System.out.println(mystr.trim());
        //Java String startsWith() and endsWith() method
        System.out.println(mystr.startsWith("ja"));
        System.out.println(mystr.endsWith("ng"));
        //Java String charAt() Method
        System.out.println(mystr.charAt(8));
        //Java String length() Method
        System.out.println(mystr.length());
        //Java String valueOf() Method
        System.out.println();
    }
}
